package javaConcepts.core.primitiveDataTypes;

public class BooleanDataType {

    public static void main(String[] args) {

        boolean trueValue1 = true;

        boolean falseValue1 = false;

        boolean value = 1 > 2;

        boolean trueValue2 = Boolean.TRUE;

        boolean falseValue2 = Boolean.FALSE;

        System.out.println("trueValue1 = " + trueValue1);
        System.out.println("falseValue1 = " + falseValue1);
        System.out.println("value = " + value);
        System.out.println("trueValue2 = " + trueValue2);
        System.out.println("falseValue2 = " + falseValue2);

    }
}
